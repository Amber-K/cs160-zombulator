function setup() {
	createCanvas(800, 800);
}

function draw() {
	fill(255, 0, 0);
	ellipse(50, 50, 80, 80);
	fill(150, 150, 200);
	ellipse(200, 100, 80, 80);
	fill(100, 0, 200);
	ellipse(500, 500, 90, 90);
	fill(200, 100, 200);
	ellipse(100, 300, 20, 20);
	fill(0, 100, 200);
	ellipse(400, 150, 150, 150);
}